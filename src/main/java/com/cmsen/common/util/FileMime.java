    private String extension;
    private String mimeType;

    FileMime(String mimeType) {
        this.mimeType = mimeType;
    }

    FileMime(String extension, String mimeType) {
        this.extension = extension;
        this.mimeType = mimeType;
    }

    public String getExtension() {
        return extension;
    }

    public static String getExtension(FileMime filename) {
        return filename.getExtension();
    }

    public String getMimeType() {
        return mimeType;
    }

    public static String get(String filename) {
        return get(filename, "");
    }

    public static String get(FileMime filename) {
        return filename.getMimeType();
    }

    public static String get(String filename, String defaultMimeType) {
        filename = getExtension(filename);
        for (FileMime mime : values()) {
            if (mime.getExtension() != null && mime.getExtension().equals(filename)) {
                return mime.getMimeType();
            }
        }
        return defaultMimeType;
    }

    public static String get(String filename, FileMime defaultMimeType) {
        return get(filename, defaultMimeType.getMimeType());
    }

    public static String get(File file) {
        return get(file.getName(), "");
    }

    public static String get(File file, String defaultMimeType) {
        return get(file.getName(), defaultMimeType);
    }

    public static String get(File file, FileMime defaultMimeType) {
        return get(file.getName(), defaultMimeType);
    }

    public static boolean has(String filename) {
        if (filename == null) {
            return false;
        }
        filename = getExtension(filename);
        if (filename.length() == 0) {
            return false;
        }
        for (FileMime mime : values()) {
            if (mime.getExtension() != null && mime.getExtension().equals(filename)) {
                return true;
            }
        }
        return false;
    }

    private static String getExtension(String filename) {
        filename = (filename == null ? "" : filename).toLowerCase();
        int i = filename.lastIndexOf(".");
        if (i == -1) {
            return filename;
        }
        return filename.substring(i + 1);
    }

package com.cmsen.common.util;

import java.io.Closeable;

public class CloseUtil {
    public static void close(Closeable... o) {
        for (Closeable closeable : o) {
            close(closeable);
        }
    }

    public static void close(Closeable closeable) {
        try {
            if (closeable != null) {
                closeable.close();
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
}
